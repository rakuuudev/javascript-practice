//addition
function add7(number) {
    return number + 7;
}

alert(add7(7));
console.log(add7(3));

//multiply
function multiply(a, b) {
    return a * b;
}

alert(multiply(7, 4));

//capitalize with touppercase
function capitalize (capt) {
    return capt.toUpperCase();
}

alert(capitalize("aku sayang tya"));

//last letter with charAt
function lastLetter(letter) {
    return letter.charAt(letter.length - 1);
}

alert(lastLetter('aku sayang tya'));